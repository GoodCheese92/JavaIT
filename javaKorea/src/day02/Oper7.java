package day02;

public class Oper7 {
	public static void main(String[] args) {
		// 연산자 (연산기호, 특정한 기능)
		// 1. 산술연산자 : +, -, *, /, %(나머지)
		// 2. 대입연산자 : =
		// 3. 누적연산자 : +=, -=, *=, /=
		// 4. 증감연산자 : ++, --
		// 5. 비교연산자 : >, <, >=, <=, ==, !=
		// 6. 논리연산자 : &&(And), ||(or), !(not)
		
		// 삼항연산자 : 조건에 따라서 프로그램 동작을 다르게
		int num = 3;
		String result = (num > 2) ? "맞다" : "틀리다";	// 조건이 맞으면 왼쪽, 조건이 틀리면 오른쪽 출력
		System.out.println(result);
		
		
		
		
	}

}
